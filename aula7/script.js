function quantPernas(pernas) {
    if(pernas == 2){
        alert("O animal é um SER HUMANO!")
    }
    else if (pernas == 4){
        alert("O animal é um CACHORRO!")
    }
    else if (pernas == 6){
        alert("O animal é uma FORMIGA!")
    }
    else if (pernas == 8){
        alert("O animal é uma ARANHA!")
    }
    else {
        alert("O animal é desconhecido.")
    }
};

quantPernas();